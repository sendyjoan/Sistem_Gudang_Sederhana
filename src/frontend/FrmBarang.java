    private void tblBarangMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblBarangMouseClicked
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel)tblBarang.getModel();
        int row = tblBarang.getSelectedRow();
        Barang brg = new Barang();
        
        brg = brg.getById(Integer.parseInt(model.getValueAt(row, 0).toString()));
        
        txtIdBarang.setText(String.valueOf(brg.getIdBarang()));
        txtNamaBarang.setText(brg.getNamaBarang());
        txtHarga.setText(String.valueOf(brg.getHargaBarang()));
        cmbSatuan.getModel().setSelectedItem(brg.getSatuan());
        txtJumlahStock.setText(String.valueOf(brg.getStock()));
        txtKeterangan.setText(brg.getKeterangan());
    }//GEN-LAST:event_tblBarangMouseClicked

