    public void tampilkanData(){
        String[] kolom = {"ID", "Nama Barang", "Harga", "Stock", "Satuan", "Keterangan"};
        ArrayList<Barang> list = new Barang().getAll();
        Object rowData[] = new Object[6];
        
        tblBarang.setModel(new DefaultTableModel(new Object[][] {}, kolom));
        
        for(int i = 0; i < list.size(); i++){
            rowData[0] = list.get(i).getIdBarang();
            rowData[1] = list.get(i).getNamaBarang();
            rowData[2] = list.get(i).getHargaBarang();
            rowData[3] = list.get(i).getStock();
            rowData[4] = list.get(i).getSatuan().getNamaSatuan();
            rowData[5] = list.get(i).getKeterangan();
            
            ((DefaultTableModel)tblBarang.getModel()).addRow(rowData);
        }
    }
    
    public void cari(String keyword){
        String[] kolom = {"ID", "Nama Barang", "Harga", "Stock", "Satuan", "Keterangan"};
        ArrayList<Barang> list = new Barang().search(keyword);
        Object rowData[] = new Object[6];
        
        tblBarang.setModel(new DefaultTableModel(new Object[][] {}, kolom));
        
        for(Barang brg : list){
            rowData[0] = brg.getIdBarang();
            rowData[1] = brg.getNamaBarang();
            rowData[2] = brg.getHargaBarang();
            rowData[3] = brg.getStock();
            rowData[4] = brg.getSatuan().getNamaSatuan();
            rowData[5] = brg.getKeterangan();
            
            ((DefaultTableModel)tblBarang.getModel()).addRow(rowData);
        }
    }
    
    public void tampilkanCmbSatuan(){
        cmbSatuan.setModel(new DefaultComboBoxModel(new Satuan().getAll().toArray()));
    }

