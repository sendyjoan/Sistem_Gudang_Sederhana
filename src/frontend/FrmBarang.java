    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        // TODO add your handling code here:
        Barang brg = new Barang();
        brg.setIdBarang(Integer.parseInt(txtIdBarang.getText()));
        brg.setNamaBarang(txtNamaBarang.getText());
        brg.setHargaBarang(Integer.parseInt(txtHarga.getText()));
        brg.setStock(Integer.parseInt(txtJumlahStock.getText()));
        brg.setSatuan((Satuan)cmbSatuan.getSelectedItem());
        brg.setKeterangan(txtKeterangan.getText());
        brg.save();
        
        txtIdBarang.setText(Integer.toString(brg.getIdBarang()));
        tampilkanData();
    }//GEN-LAST:event_btnSimpanActionPerformed

