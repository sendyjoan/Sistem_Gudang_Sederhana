    private void tblBarangKeluarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblBarangKeluarMouseClicked
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel)tblBarangKeluar.getModel();
        int row = tblBarangKeluar.getSelectedRow();
        BarangKeluar brgkel = new BarangKeluar();
        
        brgkel = brgkel.getById(Integer.parseInt(model.getValueAt(row, 0).toString()));
        
        txtIdKeluar.setText(String.valueOf(brgkel.getIdKeluar()));
        txtIdPelanggan.setText(String.valueOf(brgkel.getPelanggan().getIdPelanggan()));
        txtIdBarang.setText(String.valueOf(brgkel.getBarang().getIdBarang()));
        txtTanggalKeluar.setText(brgkel.getTanggal());
        txtJumlah.setText(String.valueOf(brgkel.getJumlahBarang()));
    }//GEN-LAST:event_tblBarangKeluarMouseClicked

