    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        // TODO add your handling code here:
        BarangKeluar brgkel = new BarangKeluar();
        brgkel.setIdKeluar(Integer.parseInt(txtIdKeluar.getText()));
        brgkel.setTanggal(txtTanggalKeluar.getText());
        brgkel.setJumlahBarang(Integer.parseInt(txtJumlah.getText()));
        brgkel.setPelanggan(new Pelanggan().getById(Integer.parseInt(txtIdPelanggan.getText())));
        brgkel.setBarang(new Barang().getById(Integer.parseInt(txtIdBarang.getText())));
        
        int stok = (brgkel.getBarang().getStock() - brgkel.getJumlahBarang());
        brgkel.getBarang().setStock(stok);
        brgkel.getBarang().save();
        brgkel.save();
        
        txtIdKeluar.setText(Integer.toString(brgkel.getIdKeluar()));
        
        tampilkanData();
    }//GEN-LAST:event_btnSimpanActionPerformed

