    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel)tblBarangKeluar.getModel();
        int row = tblBarangKeluar.getSelectedRow();
        
        BarangKeluar brgkel = new BarangKeluar().getById(Integer.parseInt(model.getValueAt(row, 0).toString()));
        
        int stock = (brgkel.getBarang().getStock() + brgkel.getJumlahBarang());
        brgkel.getBarang().setStock(stock);
        brgkel.getBarang().save();
        
        brgkel.delete();
        kosongkanForm();
        tampilkanData();
    }//GEN-LAST:event_btnHapusActionPerformed

